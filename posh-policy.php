<h5 class="mb-10">PROCEDURES FOR RESOLUTION AND SETTLEMENT OF ACTS OF SEXUAL  </h5>
<h6>HARASSMENT</h6>
<p>Predulive is committed to provide a supportive environment to resolve the concerns of Sexual Harassment as under:
</p>
<h6 class="mb-10">
Informal Resolution Options:
 </h6>
<p>When an incident of Sexual Harassment occurs, the Aggrieved Woman can communicate his/her                 disapproval and objections immediately to the Respondent and request the Respondent to desist        from such conduct in the future. If the Sexual Harassment does not stop or if the Aggrieved Woman is not comfortable with addressing the Respondent directly, he/she can bring the concern to the attention of the Internal Committee for redressal of grievances. The Internal Committee will thereafter provide advice or extend support as requested and will do the needful to resolve the matter. It is advisable that the Aggrieved Woman keeps a record of the incident i.e., date, place, possible witness and file a complaint as soon as possible.

</p>
<h6 class="mb-10">Complaint of sexual harassment:
 </h6>
<p>	An Aggrieved Woman with a Sexual Harassment concern, who is not comfortable with the informal resolution option or has exhausted such option, may make a formal complaint to the Presiding Officer or any member of the Internal Committee.
</p>
<p>Any Aggrieved Woman may make, in writing, a complaint of Sexual Harassment of Women at Workplace to the Internal Committee, within a period of 3 (three) months from the date of incident and in case of a series of incidents, within a period of three months from the date of last incident.</p>
<p>The Internal Committee, for the reasons to be recorded in writing, may decide to extend the                                   time limit not exceeding three months, if it is satisfied that the circumstances were such which  prevented the Aggrieved Woman from filing a complaint within the said period.
</p>
<p>Where the Aggrieved Woman is unable to make a complaint on account of his/her physical or mental incapacity or death or otherwise, his/her legal heir, relative, friend, co-worker or such other person as may be prescribed in the POSH Rules may make a complaint.
